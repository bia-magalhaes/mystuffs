# TIPOS DE VARIÁVEIS

# STRING: Palavras, frases, etc. Sempre entre aspas
# exemplo: "bianca"

puts "\n\n"
puts "exemplo de strings"
string_01 = "batata frita"
string_02 = "calabresa frita"

puts "variáveis: #{string_01}, #{string_02}"
puts "\n\n"

# INTEGER: Números inteiros - idades, ano, etc. Sem aspas. 
# exemplo: 1989, 45, -5 

puts "exemplo de integer"
inteiro_01 = 65
inteiro_02 = -13

puts "variáveis: #{inteiro_01}, #{inteiro_02}"
puts "\n\n"

# FLOAT: Números reais decimais.
# exemplo: 1.2, 3.47, -5.4
# OBS: Ruby obedece a anotação americana, por isso se usa ponto (.), não vírgula (,)
puts "exemplo de float"
decimal_01 = 2.5
decimal_02 = -9.6

puts "variáveis: #{decimal_01}, #{decimal_02}"
puts "\n\n"

# BOOLEAN: verdadeiro ou falso
# exemplo: true, false
# OBS: Quando eu não atribuo valor p/ ele, ele já é falso.
puts "exemplo de boolean"
verdade_sim = true
mentira_nao = false

puts "variáveis: #{verdade_sim}, #{mentira_nao}"
puts "\n\n"

# ARRAY: Conjuntos (numéricos, palavras, o que for)
# exemplo: [32, 38, 45] << sempre usando colchetes
